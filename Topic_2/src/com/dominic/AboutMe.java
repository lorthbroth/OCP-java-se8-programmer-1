package com.dominic;

public final class AboutMe {
	
	private String name;
	private String javaLevel;
	private int javaPercentage;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getJavaLevel() {
		return javaLevel;
	}

	public void setJavaLevel(String javaLevel) {
		this.javaLevel = javaLevel;
	}

	public int getJavaPercentage() {
		return javaPercentage;
	}

	public void setJavaPercentage(int javaPercentage) {
		this.javaPercentage = javaPercentage;
	}

}
